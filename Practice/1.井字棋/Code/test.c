#define _CRT_SECURE_NO_WARNINGS 1 
//测试三子棋游戏
#include"game.h"
//游戏的整个算法实现
void game() {
	char ret = 0;//判断谁赢。
	// 数组--存放玩家和电脑走出的棋盘信息；ROW行； COL列；
	char board[ROW][COL] = {0};
	//初始化棋盘
	InitBoard(board, ROW, COL);
	//打印棋盘
	DisplayBoard(board, ROW, COL);
	//下棋
	while (1) {
		//玩家下棋
		Player(board, ROW, COL);
		DisplayBoard(board, ROW, COL);
		//判断玩家是否赢
		ret = Iswin(board, ROW, COL);
		if (ret != 'C') {
			break;
		}
		
		//电脑下棋
		Computer(board, ROW, COL);
		DisplayBoard(board, ROW, COL);
		//判断电脑是否赢
		ret = Iswin(board, ROW, COL);
		if (ret != 'C') {
			break;
		}
		
	}
	if (ret == '*') {
		puts("玩家赢！");
	}
	else if (ret == '#') {
		puts("电脑赢！");
	}
	else
	{
		puts("平局！");
	}
}
void menu()//菜单
{
	puts("1.play   0.exit");
}
void test() {
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		printf("请选择：>");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
				break;
        default:
			printf("选择错误，请重新选择！\n");
			break;
		}
	} while (input);


}
int main() {

	test();
	return 0;
}